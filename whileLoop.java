// Java program while loop example
public class whileLoop
{
    public static void main(String args[])
    {
        
        //Initialize constant
        int x = 0;
  
        // Exit when x becomes greater than 4
        while (x <= 10)
        {
            System.out.println("Value of x:" + x);
  
            // Increment the value of x for
            // next iteration
            x++;
        }
    }
}